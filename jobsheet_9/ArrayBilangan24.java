public class ArrayBilangan24 {
    public static void main(String[] args) {
        
        //Deklarasi dan inisialisasikan array bertipe int
        int[] bil = {5, 13, -7, 17};

        //Cetak array menggunakan FOR
        for (int i = 0; i < 4; i++) {
            System.out.println(bil[i]);
        }

    }
}